package com.udecar.Datos;

public class Frenos {
    private int idFrenos;
    private String nombreFrenos;

    public Frenos(){

    }

    public int getIdFrenos() {
        return idFrenos;
    }

    public void setIdFrenos(int idFrenos) {
        this.idFrenos = idFrenos;
    }

    public String getNombreFrenos() {
        return nombreFrenos;
    }

    public void setNombreFrenos(String nombreFrenos) {
        this.nombreFrenos = nombreFrenos;
    }
}
